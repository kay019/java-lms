create table course (
    id bigint generated by default as identity (start with 1000 increment by 1),
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session (
    id bigint generated by default as identity (start with 1000 increment by 1),
    course_id bigint not null,
    cover_image_id bigint not null,
    session_status varchar(30) not null,
    registration_policy_type varchar(30) not null,
    session_fee bigint not null,
    max_student_count int not null,
    started_at timestamp not null,
    ended_at timestamp not null,
    primary key (id)
);

create table payment (
    id bigint generated by default as identity (start with 1000 increment by 1),
    session_id bigint not null,
    ns_user_id bigint not null,
    amount bigint not null,
    created_at timestamp not null,
    primary key (id)
);

create table cover_image (
    id bigint generated by default as identity (start with 1000 increment by 1),
    size bigint not null,
    type varchar(30) not null,
    width int not null,
    height int not null,
    primary key (id)
);