create table course
(
    id         bigint generated by default as identity,
    title      varchar(255) not null,
    creator_id bigint       not null,
    created_at timestamp    not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20) not null,
    password   varchar(20) not null,
    name       varchar(20) not null,
    email      varchar(50),
    created_at timestamp   not null,
    updated_at timestamp,
    primary key (id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at  timestamp,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);



create table session_image
(
    id         bigint generated by default as identity,
    file_size  bigint      not null,
    image_type varchar(10) not null,
    width      int         not null,
    height     int         not null,
    primary key (id)
);

create table session
(
    id                 bigint generated by default as identity,
    course_id          bigint      not null,
    cohort             int         not null,
    start_date         date        not null,
    end_date           date        not null,
    image_id           bigint      not null,
    status             varchar(20) not null,
    progress_status    varchar(20),
    recruitment_status varchar(20),
    session_type       varchar(20) not null,
    max_capacity       int,
    fee                bigint,
    created_at         timestamp   not null,
    updated_at         timestamp,
    primary key (id),
    foreign key (course_id) references course (id),
    foreign key (image_id) references session_image (id)
);

create table session_enrollment
(
    id                bigint generated by default as identity,
    session_id        bigint    not null,
    ns_user_id        bigint    not null,
    approved_at       timestamp,
    approved_by       bigint,
    primary key (id),
    foreign key (session_id) references session (id),
    foreign key (ns_user_id) references ns_user (id)

);

create table session_cover_images
(
    id               bigint generated by default as identity,
    session_id       bigint not null,
    session_image_id bigint not null,
    primary key (id),
    foreign key (session_id) references session (id),
    foreign key (session_image_id) references session_image (id)
);

create table enrollment_history
(
    id                bigint generated by default as identity,
    session_id        bigint      not null,
    ns_user_id        bigint      not null,
    enrollment_status varchar(20) not null,
    changed_by        bigint,
    changed_at        timestamp   not null,
    primary key (id),
    foreign key (session_id) references session (id)
);
