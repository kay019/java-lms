create table course (
    id bigint generated by default as identity(start with 1000),
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table session (
    id bigint not null generated by default as identity(start with 1000),
    course_id bigint not null,
    start_date timestamp not null,
    end_date timestamp not null,
    max_capacity int not null,
    tuition bigint not null,
    recruit varchar(30),
    session_type varchar(30),
    session_status varchar(30),
    recruitment_status varchar(30),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table cover_image (
       id bigint not null generated by default as identity(start with 1000),
       session_id bigint not null,
       size bigint,
       type varchar(30),
       width int,
       height int,
       primary key (id)
);

create table enrollment (
    id bigint not null generated by default as identity(start with 1000),
    session_id bigint not null,
    user_id bigint not null,
    selection_status varchar(30),
    enrollment_status varchar(30),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table payment(
    id bigint not null generated by default as identity (start with 1000),
    session_id bigint not null,
    user_id bigint not null,
    amount bigint not null,
    created_at timestamp not null,
    primary key (id)
)
