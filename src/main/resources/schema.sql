create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table session (
    id bigint generated by default as identity primary key,
    course_id bigint not null, -- 외래키 (course)
    start_date timestamp not null,
    end_date timestamp not null,
    image_size double,
    image_type varchar(10),
    image_width int,
    image_height int,
    status varchar(10) not null, -- SessionStatus (PREPARING, OPEN, CLOSE)
    enroll_status varchar(10),   -- EnrollStatus (NON_RECRUIT, RECRUIT)
    type varchar(10) not null,   -- SessionType (FREE, PAID)
    max_participants int,
    fee bigint
);

create table session_cover_image (
    id bigint generated by default as identity primary key,
    session_id bigint not null, -- 외래키 (session)
    image_size double not null,
    image_type varchar(10) not null,
    image_width int not null,
    image_height int not null
);

create table enrollment (
    id bigint generated by default as identity primary key,
    session_id bigint not null, -- 외래키 (session)
    user_id bigint not null,    -- 외래키 (user)
    payment_id varchar(100),    -- nullable
    status varchar(10),         -- nullable
    created_at timestamp not null
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    user_type varchar(10),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);
