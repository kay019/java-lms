create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);

create table course
(
    id           bigint generated by default as identity,
    title        varchar(255) not null,
    creator_id   bigint       not null,
    created_date timestamp    not null,
    updated_date timestamp,
    primary key (id)
);

create table session
(
    id                bigint generated by default as identity,
    course_id         bigint       not null,
    creator_id        bigint       not null,

    title             varchar(255) not null,
    contents clob not null,

    start_date        date         not null,
    end_date          date         not null,

    created_date      timestamp    not null,
    updated_date      timestamp,

    primary key (id)
);

create table cover_image
(
    id                bigint generated by default as identity,
    session_id        bigint      not null,
    cover_image_size  int         not null,
    cover_image_type  varchar(30) not null,

    dimensions_width  double      not null,
    dimensions_height double      not null,
    dimensions_ratio  double      not null,

    created_date      timestamp   not null,
    updated_date      timestamp
);

create table enrollment
(
    id             bigint generated by default as identity,
    session_id     bigint      not null,

    type           varchar(30) not null,
    tuition_fee    bigint      not null,
    max_enrollment int         not null,
    progress_status    varchar(30) not null,
    recruitment_status varchar(30) not null,

    created_date   timestamp   not null,
    updated_date   timestamp,

    primary key (id)
);

create table enrolled_user
(
    id            bigint generated by default as identity,
    enrollment_id bigint    not null,
    user_id bigint not null,
    approval_status varchar(30) not null,

    created_date  timestamp not null,
    updated_date  timestamp,

    primary key (id)
);