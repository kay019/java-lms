create table course
(
    id            bigint generated by default as identity,
    title         varchar(255) not null,
    has_selection boolean      not null,
    creator_id    bigint       not null,
    created_at    timestamp    not null,
    updated_at    timestamp    not null,
    primary key (id)
);

create table session
(
    id          bigint generated by default as identity,
    course_id   bigint         not null,
    capacity    int            not null,
    status      varchar(20)    not null,
    recruitment varchar(20)    not null,
    price       decimal(10, 2) not null,
    start_date  date           not null,
    end_date    date           not null,
    creator_id  bigint         not null,
    created_at  timestamp      not null,
    updated_at timestamp not null,
    primary key (id),
    foreign key (course_id) references course (id)
);

create table session_image
(
    id         bigint generated by default as identity,
    session_id bigint    not null,
    image_path varchar(255),
    image_file blob,
    created_at timestamp not null,
    updated_at timestamp not null,
    primary key (id),
    foreign key (session_id) references session (id)
);

create table ns_user
(
    id         bigint generated by default as identity,
    user_id    varchar(20)              not null,
    password   varchar(20)              not null,
    name       varchar(20)              not null,
    email      varchar(50),
    balance    decimal(10, 2) default 0 not null,
    created_at timestamp                not null,
    updated_at timestamp not null,
    primary key (id)
);

create table applicant
(
    id         bigint generated by default as identity,
    session_id bigint    not null,
    ns_user_id bigint    not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    primary key (id),
    foreign key (session_id) references session (id),
    foreign key (ns_user_id) references ns_user (id),
    constraint unique_session_user UNIQUE (session_id, ns_user_id)
);

create table question
(
    id         bigint generated by default as identity,
    created_at timestamp    not null,
    updated_at timestamp not null,
    contents   clob,
    deleted    boolean      not null,
    title      varchar(100) not null,
    writer_id  bigint,
    primary key (id)
);

create table answer
(
    id          bigint generated by default as identity,
    created_at  timestamp not null,
    updated_at timestamp not null,
    contents    clob,
    deleted     boolean   not null,
    question_id bigint,
    writer_id   bigint,
    primary key (id)
);

create table delete_history
(
    id            bigint not null,
    content_id    bigint,
    content_type  varchar(255),
    created_date  timestamp,
    deleted_by_id bigint,
    primary key (id)
);
